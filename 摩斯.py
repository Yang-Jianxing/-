mos = {'1': '.----', '2': '..---', '3': '...--', '4': '....-', '5': '.....',
       '6': '-....', '7': '--...', '8': '---..', '9': '----.', '0': '-----', }
n = int(input("行数："))
for _ in range(n):
    m = input("一段数字:")
    for i in m:
        r = mos[i]
        print(r, end='\t')
    print()
